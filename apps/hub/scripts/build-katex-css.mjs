import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');

const KATEX_VERSION = '0.16.45';
const FONT_BASE = `https://cdn.jsdelivr.net/npm/katex@${KATEX_VERSION}/dist/fonts/`;

const cssPath = join(root, 'node_modules/katex/dist/katex.min.css');
const outPath = join(root, 'src/lib/katex-css.generated.ts');

let css = readFileSync(cssPath, 'utf8');
css = css.replace(/url\(fonts\/([^)]+)\)/g, `url(${FONT_BASE}$1)`);

const escaped = css.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');

const out = `// Generated by scripts/build-katex-css.mjs — do not edit by hand.
export const KATEX_CSS = \`${escaped}\`;
`;

writeFileSync(outPath, out);
console.log(`\u2713 wrote ${outPath} (${css.length} bytes)`);
