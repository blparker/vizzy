import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');

// Resolve katex from our node_modules (pnpm symlinks it into our package's node_modules).
const katexRoot = resolve(root, 'node_modules/katex/dist');
const cssPath = join(katexRoot, 'katex.min.css');
const fontsDir = join(katexRoot, 'fonts');
const outPath = join(root, 'src/katex-css.generated.ts');

let css = readFileSync(cssPath, 'utf8');

// Drop non-woff2 @font-face src entries (ttf, woff) and inline the woff2 ones as
// base64 data URIs. Result: a single self-contained CSS blob with every glyph
// available in the SVG foreignObject rasterizer — no runtime CSS scraping, no
// cross-origin font fetches, no dependency on the host page loading KaTeX CSS.
css = css.replace(/url\(fonts\/([^)]+\.woff2)\)/g, (_m, filename) => {
    const fontPath = join(fontsDir, filename);
    const bytes = readFileSync(fontPath);
    return `url(data:font/woff2;base64,${bytes.toString('base64')})`;
});

// Remove remaining external font refs (ttf/woff fallbacks) so the browser
// doesn't attempt network requests — woff2 covers every evergreen target.
css = css.replace(/,\s*url\(fonts\/[^)]+\.(ttf|woff)\)\s*format\(['"][^'"]+['"]\)/g, '');

const escaped = css.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');
const out = `// Generated by scripts/build-katex-css.mjs — do not edit by hand.
export const KATEX_CSS = \`${escaped}\`;
`;

writeFileSync(outPath, out);
console.log(`\u2713 wrote ${outPath} (${(css.length / 1024).toFixed(1)} KB)`);
